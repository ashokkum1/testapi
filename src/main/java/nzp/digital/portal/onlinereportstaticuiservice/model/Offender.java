package nzp.digital.portal.onlinereportstaticuiservice.model;

public class Offender {
	private boolean seenOffender;
	private String offenderDescription;
	public boolean isSeenOffender() {
		return seenOffender;
	}
	public void setSeenOffender(boolean seenOffender) {
		this.seenOffender = seenOffender;
	}
	public String getOffenderDescription() {
		return offenderDescription;
	}
	public void setOffenderDescription(String offenderDescription) {
		this.offenderDescription = offenderDescription;
	}
	
}
